#include <iostream>

using namespace std;

int main()
{
    // Dichiarazione di variabili
    // <tipo> <nome>
    int a; //< a è una variabile di tipo intero
    char c; //< c è una variabile di tipo carattere
    float b; //< b è una variabile di tipo numero in virgola mobile
             //  (floating point) con precisione "singola" (32 bit in molte architetture)
    double d; //< d è variabile di tipo numero in virgola mobile
              // con precisione "doppia" (64 bit in molte architetture)
    /*void v;*/ //< Esiste un tipo "p"rivo di tipo
    bool l; //> l è un valore di tipo Booleano (vero o falso)

    // [<modificatore>] <tipo> <nome>
    short int si;
    long int li;
    unsigned char uc;
    signed char sc;
    long long int lli;
    unsigned long long int ulli;

    return 0;
}
