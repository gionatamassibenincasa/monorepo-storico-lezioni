#include <iostream>

using namespace std;

int main(); //< Dichiazione di tipo (prototipo di funzione)
            // main è un identificatore di una funzione
            // che non accetta parametri
            // e restituisce un valore di tipo intero

int main()
{
    // Dichiarazioni di variabili
    // <tipo> <nome_identificatore>
    char c; //< c è di tipo carattere
    int i;  //< i è di tipo intero con segno
    float f; //< f è di tipo numerico con virgola mobile a precisione singola
    double d; //< d è di tipo numerico con virgola mobile a precisione doppia
    // void v;
    bool b; //< b è di tipo booleano (vero e falso)
    // <modificatore> <tipo> <nome_identificatore>
    signed char sc;
    unsigned char uc;
    short unsigned int sui;
    short signed int ssi;
    unsigned long int uli;
    signed long int sli;
    unsigned long long int ulli;
    signed long long int slli;

    return 0;
}
